import axios from 'axios';
import { Booking, BookingFormData, ServiceType } from '../types/booking';

const API_BASE = 'http://localhost:8080/api';

const apiClient = axios.create({
  baseURL: API_BASE,
  headers: { 'Content-Type': 'application/json' },
});

apiClient.interceptors.request.use((config) => {
  const token = localStorage.getItem('token');
  if (token) {
    config.headers.Authorization = `Bearer ${token}`;
  }
    // Apply debounce to prevent rapid calls
  return config;
});

export const bookingApi = {
  getAll: () => apiClient.get<Booking[]>('/bookings'),
  getById: (id: number) => apiClient.get<Booking>(`/bookings/${id}`),
  create: (data: BookingFormData) => apiClient.post<Booking>('/bookings', data),
  cancel: (id: number) => apiClient.put<Booking>(`/bookings/${id}/cancel`),
  confirm: (id: number) => apiClient.put<Booking>(`/bookings/${id}/confirm`),
};

export const serviceApi = {
  getAll: () => apiClient.get<ServiceType[]>('/services'),
  getAvailable: () => apiClient.get<ServiceType[]>('/services/available'),
};
